package JavaBuch;

public class Kap8Uebung2 {

    public static void main(String[] args) {

        /*
         * Geben Sie die Zahlen von 20 bis 0 rückwärts in Zweierschritten aus. Verwenden
         * Sie hierfür eine Schleife, die einen möglichst geringen Programmcode erzeugt.
         */

        int i = 20;

        while (i >= 0) {

            System.out.println(i);
            i = i - 2;

        }

    }

}
